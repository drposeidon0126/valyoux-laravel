    function loadmoreData(page,artist_id){

       $.ajax({
         url : "loadmoredata",
         type: "GET",
         cache:false,
         data:{offset:page,artist_id:artist_id},
         success:function(data){
           if (data) {
               const data1 = JSON.parse(data);
               $(".loadbtnvideos").hide();
               $(".all_social_videos").append(data1.html);
           }
         }
       });
     }
