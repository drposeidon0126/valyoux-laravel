      function loadMoreData(page,user_id,id){

       $.ajax({
         url : "loadmoredata",
         type: "GET",
         cache:false,
         data:{offset:page,user_id:user_id,id:id,type:2},
         success:function(data){
           if (data) {
               const data1 = JSON.parse(data);
               $(".loadbtnvideos").hide();
               $(".all_social_videos").append(data1.html);
           }
         }
       });
     }

